function getFizzBuzz(num) {
  if (num % 3 === 0 && num % 5 === 0) {
    return 'FizzBuzz';
  } else if (num % 3 === 0) {
    return 'Fizz';
  } else if (num % 5 === 0) {
    return 'Buzz';
  } else return num;
}

function getFactorial(n) {
  if (n === 0) {
    return 1;
  }
  return n * getFactorial(n - 1);
}

function getSumBetweenNumbers(n1, n2) {
  let sum = 0;
  for (let i = n1; i <= n2; i++) {
    sum += i;
  }
  return sum;
}

function isTriangle(a, b, c) {
  return (a + b > c) && (a + c > b) && (c + b > a);
}


